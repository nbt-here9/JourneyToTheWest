    private void loadSchedule() {
        try {
            tblModelSchedule.setRowCount(0);
            for (ScheduleDTO scheduleDTO : scheduleList) {
                if (!checkDateHistory(scheduleDTO.getEndDate())) {
                    tblModelSchedule.addRow(scheduleDTO.toVector());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadHistory() {
        try {
            tblModelHistory.setRowCount(0);
            for (ScheduleDTO scheduleDTO : scheduleList) {
                if (checkDateHistory(scheduleDTO.getEndDate())) {
                    tblModelHistory.addRow(scheduleDTO.toVector());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

