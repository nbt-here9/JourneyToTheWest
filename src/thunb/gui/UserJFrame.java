    private void jLSaveProfileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLSaveProfileMouseClicked
        // TODO add your handling code here:
        String name, image, phone, email, pass;
        String valid = "";
        CastDAO daoC = new CastDAO();
        UserDAO daoU = new UserDAO();
        name = txtProfileName.getText();;
        image = imgChoose;
        phone = txtProfilePhone.getText();
        email = txtProfileEmail.getText();
        pass = new String(txtProfilePassword.getPassword());
        if (name.equals("")) {
            valid += "Name can not empty \n";
        }
        if (phone.equals("")) {
            valid += "Phone can not empty \n";
        }
        if (email.equals("")) {
            valid += "Email can not empty \n";
        }
        if (pass.equals("")) {
            valid += "Password can not empty \n";
        }
        if (valid.equals("")) {
            try {
                if (daoU.update(cast.getUsername(), pass)
                        && daoC.updateProfile(cast.getUsername(), name, image, phone, email)) {
                    JOptionPane.showMessageDialog(this, "Update successfully!");
                } else {
                    JOptionPane.showMessageDialog(this, "Update Failed!");
                }
            } catch (Exception ex) {
                Logger.getLogger(UserJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }

    }//GEN-LAST:event_jLSaveProfileMouseClicked

    private void tblViewRoleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewRoleMouseClicked
        // TODO add your handling code here:
        int row = tblViewRole.getSelectedRow();
        if (row == -1) {
            return;
        }
        String roleID = (String) tblModelRoleList.getValueAt(row, 0);
        RoleDAO dao = new RoleDAO();
        RoleDTO dto = null;
        try {
            dto = dao.getDTOByRoleID(roleID);
        } catch (Exception ex) {
            Logger.getLogger(UserJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (dto != null) {
            txtRoleNameDes.setText("Role: " + dto.getRoleName());
            String description = dao.loadRoleDesFromFile(dto.getRoleDes());
            txtRoleDesForDownlooad.setText(description);
        }
    }//GEN-LAST:event_tblViewRoleMouseClicked

