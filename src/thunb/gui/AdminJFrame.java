    private void tblViewTriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewTriMouseClicked
        // TODO add your handling code here:
        int row = tblViewTri.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = (String) tblModelTri.getValueAt(row, 0);
        triIDCurrent = id;

        TribulationDTO dto = null;
        TribulationDAO dao = new TribulationDAO();
        try {
            dto = dao.getDTOByID(id);
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (dto != null) {
            txtTriName.setText("");
            txtTriName.setText(dto.getName());
            triNameCurrent = dto.getName();
            txtTriSD.setText(dto.getStartDate());
            txtTriED.setText(dto.getEndDate());
            txtTriLocate.setText(dto.getLocate());
            txtTriNG.setText(dto.getNumNG() + "");
            jTextAreaTriDes.setText(dto.getDes());
        } else {
            JOptionPane.showMessageDialog(this, "Failed");
        }

    }//GEN-LAST:event_tblViewTriMouseClicked

    private void tblViewRoleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewRoleMouseClicked
        // TODO add your handling code here:
        int row = tblViewRole.getSelectedRow();
        String id = (String) tblModelRole.getValueAt(row, 0);
        if (row == -1) {
            return;
        }
        RoleDTO dto = null;
        RoleDAO dao = new RoleDAO();
        try {
            dto = dao.getDTOByRoleID(id);
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (dto != null) {
            txtRoleName.setText(dto.getRoleName());
            String description = dao.loadRoleDesFromFile(dto.getRoleDes());
            jTextAreaRoleDes.setText(description);
            if (dto.getCastID() != null) {
                txtRoleCastName.setText(dto.getCastID() + " - " + dto.getCastName());
            } else {
                txtRoleCastName.setText("");
                cbbCastName.setSelectedIndex(0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Failed");
        }
    }//GEN-LAST:event_tblViewRoleMouseClicked

