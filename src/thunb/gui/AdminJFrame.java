    private void loadAllTriPropsAfter() throws Exception {
        PropstDAO dao = new PropstDAO();
        List<PropsDTO> list = dao.getAllDTO();
        PropsUsedDAO daoU = new PropsUsedDAO();
        List<PropsUsedDTO> listU = daoU.getAllDTOByTriID(triIDCurrent);
        for (PropsUsedDTO pU : listU) {
            for (PropsDTO p : list) {
                if (p.getPropsID().equals(pU.getPropsID())) {
                    p.setQuantity(p.getQuantity() - pU.getQuantity());
                }
            }
        }
        tblModelPropsUsedAllProps.setRowCount(0);
        for (PropsDTO propsDTO : list) {
            tblModelPropsUsedAllProps.addRow(propsDTO.toVector());
        }
    }

